package com.greatwall.recharge.dto;

public class ChannelCondition {

	public Integer cm;
	public Integer cu;
	public Integer ct;
	public Integer cmflow;
	public Integer cuflow;
	public Integer ctflow;
	public Integer getCm() {
		return cm;
	}
	public void setCm(Integer cm) {
		this.cm = cm;
	}
	public Integer getCu() {
		return cu;
	}
	public void setCu(Integer cu) {
		this.cu = cu;
	}
	public Integer getCmflow() {
		return cmflow;
	}
	public void setCmflow(Integer cmflow) {
		this.cmflow = cmflow;
	}
	public Integer getCuflow() {
		return cuflow;
	}
	public void setCuflow(Integer cuflow) {
		this.cuflow = cuflow;
	}
	public Integer getCt() {
		return ct;
	}
	public void setCt(Integer ct) {
		this.ct = ct;
	}
	public Integer getCtflow() {
		return ctflow;
	}
	public void setCtflow(Integer ctflow) {
		this.ctflow = ctflow;
	}
	
	
}
